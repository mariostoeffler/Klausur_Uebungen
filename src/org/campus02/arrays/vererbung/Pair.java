package org.campus02.arrays.vererbung;

public class Pair<K, V>{

    private K key;
    private V value;

    public Pair(Person person, double bmi) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }
}
